import { describe, expect, it } from 'vitest'
import { DvalaError } from '../errors'
import { asString, assertString } from './string'

describe('string type guards', () => {
  it('asNonEmptyString', () => {
    expect(asString('1', undefined, { nonEmpty: true })).toBe('1')
    expect(() => asString('', undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(0, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(1, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(true, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(false, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(null, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString(undefined, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString([], undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => asString({}, undefined, { nonEmpty: true })).toThrow(DvalaError)
  })

  it('assertString', () => {
    expect(() => assertString('', undefined)).not.toThrow()
    expect(() => assertString('1', undefined)).not.toThrow()
    expect(() => assertString(0, undefined)).toThrow(DvalaError)
    expect(() => assertString(1, undefined)).toThrow(DvalaError)
    expect(() => assertString(true, undefined)).toThrow(DvalaError)
    expect(() => assertString(false, undefined)).toThrow(DvalaError)
    expect(() => assertString(null, undefined)).toThrow(DvalaError)
    expect(() => assertString(undefined, undefined)).toThrow(DvalaError)
    expect(() => assertString([], undefined)).toThrow(DvalaError)
    expect(() => assertString({}, undefined)).toThrow(DvalaError)
  })
  it('asString', () => {
    expect(() => asString('', undefined)).not.toThrow()
    expect(() => asString('1', undefined)).not.toThrow()
    expect(() => asString(0, undefined)).toThrow(DvalaError)
    expect(() => asString(1, undefined)).toThrow(DvalaError)
    expect(() => asString(true, undefined)).toThrow(DvalaError)
    expect(() => asString(false, undefined)).toThrow(DvalaError)
    expect(() => asString(null, undefined)).toThrow(DvalaError)
    expect(() => asString(undefined, undefined)).toThrow(DvalaError)
    expect(() => asString([], undefined)).toThrow(DvalaError)
    expect(() => asString({}, undefined)).toThrow(DvalaError)
  })
  it('assertNonEmptyString', () => {
    expect(() => assertString('1', undefined, { nonEmpty: true })).not.toThrow()
    expect(() => assertString('abc', undefined, { nonEmpty: true })).not.toThrow()
    expect(() => assertString('', undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(0, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(1, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(true, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(false, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(null, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString(undefined, undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString([], undefined, { nonEmpty: true })).toThrow(DvalaError)
    expect(() => assertString({}, undefined, { nonEmpty: true })).toThrow(DvalaError)
  })

  it('assertChar', () => {
    expect(() => assertString('2', undefined, { char: true })).not.toThrow()
    expect(() => assertString('Albert', undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(0, undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(null, undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(true, undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(false, undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(['a'], undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString({ a: 'a' }, undefined, { char: true })).toThrow(DvalaError)
  })
  it('asChar', () => {
    expect(asString('2', undefined, { char: true })).toBe('2')
    expect(() => asString('Albert', undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString(0, undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString(null, undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString(true, undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString(false, undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString(['a'], undefined, { char: true })).toThrow(DvalaError)
    expect(() => asString({ a: 'a' }, undefined, { char: true })).toThrow(DvalaError)
  })

  it('character', () => {
    expect(() => assertString('k', undefined, { char: true })).not.toThrow()
    expect(() => assertString('k1', undefined, { char: true })).toThrow(DvalaError)
    expect(() => assertString(1, undefined, { char: true })).toThrow(DvalaError)
  })
})
